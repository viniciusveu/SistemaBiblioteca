    public UI_Emprestar(Biblioteca b) {
        initComponents();
        this.model = (DefaultTableModel) jTable1.getModel();
        this.b = b;

        Object obj[] = new Object[4];
        
        for (int i = 0; i < b.getUsuarios().size(); i++) {
            obj[0] = b.getUsuarios().get(i).getCodUsuario();
            obj[1] = b.getUsuarios().get(i).getNome();
            
            if(b.getUsuarios().get(i) instanceof Aluno){
                obj[2] = "Aluno";
            }else{
                obj[2] = "Professor";
            }
            
            
            obj[3] = b.getUsuarios().get(i).getDiasEmprestimo();
            
            model.addRow(obj);
        }
        

        for (int i = 0; i < b.getLivros().size(); i++) {
            lista.addElement(b.getLivros().get(i));
        }
        lstLivros.setModel(lista);
        lstLivrosSelecionados.setModel(listaSelecionados);
    }

